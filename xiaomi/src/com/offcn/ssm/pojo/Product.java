package com.offcn.ssm.pojo;

public class Product {
	private String pid;
	private String pname;
	private String pimage;
	private String pdate;
	private String pdesc;
	private Double shop_price;
	private Double market_price;
	private Integer is_hot;//表示是否热门
	private Integer is_latest;
	private Integer pflag;//表示是否下架
	private Category category;//表示该商品所属的分类信息
	private String cid;
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPimage() {
		return pimage;
	}
	public void setPimage(String pimage) {
		this.pimage = pimage;
	}
	public String getPdate() {
		return pdate;
	}
	public void setPdate(String pdate) {
		this.pdate = pdate;
	}
	public String getPdesc() {
		return pdesc;
	}
	public void setPdesc(String pdesc) {
		this.pdesc = pdesc;
	}
	public Double getShop_price() {
		return shop_price;
	}
	public void setShop_price(Double shop_price) {
		this.shop_price = shop_price;
	}
	public Double getMarket_price() {
		return market_price;
	}
	public void setMarket_price(Double market_price) {
		this.market_price = market_price;
	}
	public Integer getIs_hot() {
		return is_hot;
	}
	public void setIs_hot(Integer is_hot) {
		this.is_hot = is_hot;
	}
	
	public Integer getIs_latest() {
		return is_latest;
	}
	public void setIs_latest(Integer is_latest) {
		this.is_latest = is_latest;
	}
	
	public Integer getPflag() {
		return pflag;
	}
	public void setPflag(Integer pflag) {
		this.pflag = pflag;
	}
	public Category getCategory() {
		return category;
	}
	public void setCategory(Category category) {
		this.category = category;
	}
	@Override
	public String toString() {
		return "Product [pid=" + pid + ", pname=" + pname + ", pimage=" + pimage + ", pdate=" + pdate + ", pdesc="
				+ pdesc + ", shop_price=" + shop_price + ", market_price=" + market_price + ", is_hot=" + is_hot
				+ ", pflag=" + pflag + ", category=" + category + "]";
	}
}
